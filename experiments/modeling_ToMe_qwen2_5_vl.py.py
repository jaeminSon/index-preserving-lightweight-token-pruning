    def adjust_tokens(self, input_ids, input_embeds, list_n_img_tokens):
        """
        This function processes input token IDs and embeddings to adjust the placement of image tokens within a sequence.

        Args:
            input_ids: torch.Tensor, input token ids
            input_embeds: torch.Tensor, input embedding features
            list_n_img_tokens: List of int, specifying the number of image tokens per image tag.

        Returns:
            new_input_ids: adjusted input_ids
            new_input_embeds: adjusted input_embeds
        """
        assert (len(input_ids.shape) == 2) and (
            input_ids.shape[0] == 1
        ), "Argument input_ids should have a shape (1, *)."
        assert (len(input_embeds.shape) == 3) and input_embeds.shape[
            0
        ] == 1, "Argument input_embeds should have a shape (1, *, *)."

        mask_non_img_token = input_ids[0] != self.config.image_token_id
        right_shifted_mask = torch.concat(
            [
                torch.zeros(1).to(torch.bool).to(mask_non_img_token),
                mask_non_img_token[:-1],
            ]
        )
        left_shifted_mask = torch.concat(
            [
                mask_non_img_token[1:],
                torch.zeros(1).to(torch.bool).to(mask_non_img_token),
            ]
        )
        starts_non_img_token = torch.nonzero(
            (mask_non_img_token & (~right_shifted_mask)), as_tuple=True
        )[0]
        ends_non_img_token = torch.nonzero(
            (mask_non_img_token & (~left_shifted_mask)), as_tuple=True
        )[0]

        assert (
            starts_non_img_token.size()[0]
            == ends_non_img_token.size()[0]
            == len(list_n_img_tokens) + 1
        ), "Image token chunks do not match in number."

        new_input_ids = torch.zeros(
            mask_non_img_token.sum() + sum(list_n_img_tokens),
            device=input_ids.device,
            dtype=input_ids.dtype,
        )
        new_input_embeds = torch.zeros(
            (
                1,
                mask_non_img_token.sum() + sum(list_n_img_tokens),
                input_embeds.shape[-1],
            ),
            device=input_embeds.device,
            dtype=input_embeds.dtype,
        )

        s = 0
        for i in range(starts_non_img_token.size()[0]):

            # non-img tokens
            s_ori = starts_non_img_token[i]
            e_ori = ends_non_img_token[i]
            new_input_ids[s : s + e_ori - s_ori + 1] = input_ids[0, s_ori : e_ori + 1]
            new_input_embeds[0, s : s + e_ori - s_ori + 1, :] = input_embeds[
                0, s_ori : e_ori + 1, :
            ]
            s += e_ori - s_ori + 1

            # img tokens
            if i < len(list_n_img_tokens):
                n_img_tokens = list_n_img_tokens[i]
                new_val = torch.repeat_interleave(
                    torch.tensor([self.config.image_token_id]), n_img_tokens
                )
                new_input_ids[s : s + n_img_tokens] = new_val
                # input_embeds[0, s, :] is image token feature
                new_input_embeds[0, s : s + n_img_tokens] = input_embeds[0, s, :]
                s += n_img_tokens

        new_input_ids = new_input_ids.unsqueeze(0)

        return new_input_ids, new_input_embeds

    @add_start_docstrings_to_model_forward(QWEN2_5_VL_INPUTS_DOCSTRING)
    @replace_return_docstrings(
        output_type=Qwen2_5_VLCausalLMOutputWithPast, config_class=_CONFIG_FOR_DOC
    )
    def forward(
        self,
        input_ids: Optional[torch.LongTensor] = None,
        attention_mask: Optional[torch.Tensor] = None,
        position_ids: Optional[torch.LongTensor] = None,
        past_key_values: Optional[List[torch.FloatTensor]] = None,
        inputs_embeds: Optional[torch.FloatTensor] = None,
        labels: Optional[torch.LongTensor] = None,
        use_cache: Optional[bool] = None,
        output_attentions: Optional[bool] = None,
        output_hidden_states: Optional[bool] = None,
        return_dict: Optional[bool] = None,
        pixel_values: Optional[torch.Tensor] = None,
        pixel_values_videos: Optional[torch.Tensor] = None,
        image_grid_thw: Optional[torch.LongTensor] = None,
        video_grid_thw: Optional[torch.LongTensor] = None,
        rope_deltas: Optional[torch.LongTensor] = None,
        cache_position: Optional[torch.LongTensor] = None,
        second_per_grid_ts: Optional[torch.Tensor] = None,
    ) -> Union[Tuple, Qwen2_5_VLCausalLMOutputWithPast]:
        r"""

        Returns:
            Qwen2_5_VLCausalLMOutputWithPast

        ```python
        >>> from PIL import Image
        >>> import requests
        >>> from transformers import AutoProcessor, SEPrune_Qwen2_5_VLForConditionalGeneration

        >>> model = SEPrune_Qwen2_5_VLForConditionalGeneration.from_pretrained("Qwen/Qwen2.5-VL-7B-Instruct")
        >>> processor = AutoProcessor.from_pretrained("Qwen/Qwen2.5-VL-7B-Instruct")

        >>> messages = [
            {
                "role": "user",
                "content": [
                    {"type": "image"},
                    {"type": "text", "text": "What is shown in this image?"},
                ],
            },
        ]
        >>> url = "https://www.ilankelman.org/stopsigns/australia.jpg"
        >>> image = Image.open(requests.get(url, stream=True).raw)

        >>> text = processor.apply_chat_template(messages, tokenize=False, add_generation_prompt=True)
        >>> inputs = processor(text=[text], images=[image], vision_infos=[vision_infos])

        >>> # Generate
        >>> generate_ids = model.generate(inputs.input_ids, max_length=30)
        >>> tokenizer.batch_decode(generate_ids, skip_special_tokens=True, clean_up_tokenization_spaces=False)[0]
        "The image shows a street scene with a red stop sign in the foreground. In the background, there is a large red gate with Chinese characters ..."
        ```"""

        output_attentions = (
            output_attentions
            if output_attentions is not None
            else self.config.output_attentions
        )
        output_hidden_states = (
            output_hidden_states
            if output_hidden_states is not None
            else self.config.output_hidden_states
        )
        return_dict = (
            return_dict if return_dict is not None else self.config.use_return_dict
        )

        if inputs_embeds is None:
            inputs_embeds = self.model.embed_tokens(input_ids)
            if pixel_values is not None:
                pixel_values = pixel_values.type(self.visual.dtype)
                image_embeds, img_pos_index, list_n_img_tokens = self.visual(
                    pixel_values,
                    indices=torch.arange(pixel_values.shape[0]//4, dtype=torch.long, device=pixel_values.device),
                    grid_thw=image_grid_thw,
                )

                input_ids, inputs_embeds = self.adjust_tokens(
                    input_ids, inputs_embeds, list_n_img_tokens
                )

                mask = input_ids == self.config.image_token_id
                mask_unsqueezed = mask.unsqueeze(-1)
                mask_expanded = mask_unsqueezed.expand_as(inputs_embeds)
                image_mask = mask_expanded.to(inputs_embeds.device)

                image_embeds = image_embeds.to(
                    inputs_embeds.device, inputs_embeds.dtype
                )
                inputs_embeds = inputs_embeds.masked_scatter(image_mask, image_embeds)
                self.token_length = input_ids.size()[-1]

            # update attention mask
            attention_mask = torch.ones_like(input_ids)
            attention_mask = attention_mask.to(inputs_embeds.device)

        # if we get 4D attention mask we cannot calculate rope deltas anymore. TODO @raushan fixme
        if position_ids is None and (
            attention_mask is None or attention_mask.ndim == 2
        ):
            # calculate RoPE index once per generation in the pre-fill stage only
            if (
                (cache_position is not None and cache_position[0] == 0)
                or self.rope_deltas is None
                or (past_key_values is None or past_key_values.get_seq_length() == 0)
            ):
                position_ids, rope_deltas = self.get_rope_index(
                    input_ids,
                    img_pos_index,
                    image_grid_thw,
                    video_grid_thw,
                    second_per_grid_ts,
                    attention_mask=None,
                )
                self.rope_deltas = rope_deltas
            # then use the prev pre-calculated rope-deltas to get the correct position ids
            else:
                batch_size, seq_length, _ = inputs_embeds.shape
                delta = (self.token_length + self.rope_deltas).to(inputs_embeds.device)
                position_ids = torch.arange(seq_length, device=inputs_embeds.device)
                position_ids = position_ids.view(1, -1).expand(batch_size, -1)
                delta = delta.repeat_interleave(batch_size // delta.shape[0], dim=0)
                position_ids = position_ids.add(delta)
                position_ids = position_ids.unsqueeze(0).expand(3, -1, -1)
                self.token_length += seq_length

        outputs = self.model(
            input_ids=None,
            position_ids=position_ids,
            attention_mask=attention_mask,
            past_key_values=past_key_values,
            inputs_embeds=inputs_embeds,
            use_cache=use_cache,
            output_attentions=output_attentions,
            output_hidden_states=output_hidden_states,
            return_dict=return_dict,
            cache_position=cache_position,
        )

        hidden_states = outputs[0]
        logits = self.lm_head(hidden_states)

        loss = None
        if labels is not None:
            # Upcast to float if we need to compute the loss to avoid potential precision issues
            logits = logits.float()
            # Shift so that tokens < n predict n
            shift_logits = logits[..., :-1, :].contiguous()
            shift_labels = labels[..., 1:].contiguous()
            # Flatten the tokens
            loss_fct = CrossEntropyLoss()
            shift_logits = shift_logits.view(-1, self.config.vocab_size)
            shift_labels = shift_labels.view(-1)
            # Enable model parallelism
            shift_labels = shift_labels.to(shift_logits.device)
            loss = loss_fct(shift_logits, shift_labels)

        if not return_dict:
            output = (logits,) + outputs[1:]
            return (loss,) + output if loss is not None else output

        return Qwen2_5_VLCausalLMOutputWithPast(
            loss=loss,
            logits=logits,
            past_key_values=outputs.past_key_values,
            hidden_states=outputs.hidden_states,
            attentions=outputs.attentions,
            rope_deltas=self.rope_deltas,
        )

